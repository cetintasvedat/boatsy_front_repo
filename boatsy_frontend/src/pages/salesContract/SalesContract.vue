      veya Rezervasyon Bedeli kadar olabilir.) Hizmet Bedeli düşüldükten sonra kalan tutar ÖHS tarafından, Çıkış
      Tarihi’nden en geç 5 iş günü sonra, bu Sözleşme’nin Bölüm – 7 isimli kısmında anlatılan İptal ve Değişiklik
