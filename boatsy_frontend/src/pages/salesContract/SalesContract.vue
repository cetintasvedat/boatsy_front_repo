      verilen Tekne ile teslim alınan Tekne’nin örtüşmediği iddiasını kanıtlayan nitelikte fotoğraflar ulaştırması
      gerekir (Tekne Sahibi, Kiracı’nın Hizmet Sağlayıcı’ya göndereceği fotoğrafların ve Kiracı’ya ait sair belgelerin
