      <br>
      Hizmet Sağlayıcı’nın yapacağı tüm ödemeler ve iadelerde, Hizmet Sağlayıcı’nın serbest takdirine göre, alınan
      ödemenin Rezervasyon tarihindeki veya Rezervasyon Talebi tarihindeki Rezervasyon Para Birimi karşılığı esas
      alınır. Hizmet Sağlayıcı döviz kurlarındaki değişikliklerden kaynaklanan zararlardan sorumlu değildir.
