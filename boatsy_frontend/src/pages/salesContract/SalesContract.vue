      REZERVASYON
      <br>
      <br>
      Rezervasyon
      <br>
      <br>
      Site üzerinden bir Kiracı, Site’de yayınlanan belirli bir İlan için bir Rezervasyon Talebi gönderebilir. Kiracı
      Rezervasyon Talebi yapar yapmaz Hizmet Sağlayıcı Tekne Sahibi’ni e-posta, kısa mesaj veya bildirim yolu ile ya da
      Site üzerinden (kendi takdirine bağlı olarak bu yollardan biri ya da birçoğu ile) bilgilendirir.
      <br>
      <br>
      Tekne Sahibi iseniz ve Hizmetler vasıtasıyla bir Rezervasyon Talebi aldıysanız, bu Rezervasyon Talebi’ni talebin
      yapıldığı andan itibaren en geç yirmi dört (24) saat içinde onaylamanız ya da reddetmeniz gerekmektedir, aksi
      takdirde Rezervasyon Talebi otomatik olarak iptal edilebilir. Eğer Giriş Tarihi’nin başlangıcı ile Rezervasyon
      Talebi’nin yapıldığı an arasındaki süre yirmi dört (24) saatten azsa, Tekne Sahibi Rezervasyon Talebi’ni Giriş
      Tarihi’nin başlangıcına (00:00) kadar onaylamalı ya da reddetmelidir.
      <br>
      <br>
      Eğer bir Tekne’nin Rezervasyon Talebi’ni bahsi geçen yirmi dört (24) saatlik süre zarfında onaylama imkanınız
      olmaz veya reddetme konusunda kararsız kalırsanız, talep edilen Rezervasyon için Hizmet Sağlayıcı tarafından
      tahsil edilen tutar ilgili Kiracı’ya iade edilebilir ve, eğer varsa, kredi kartı üzerindeki provizyon
      kaldırılabilir (bu ihtimaller, Sözleşme’nin Bölüm – 6 isimli kısmında detaylı bir şekilde anlatılmıştır).
      <br>
      <br>
      Rezervasyon, Tekne Sahibi’nin Rezervasyon Talebi’ni onaylamasıyla tamamlanır ve Kiracı ile Tekne Sahibi arasında
      (Kiracı’nın teklifi, Tekne Sahibi’nin de onayı olduğu için) MSS kurulur (Tekne Sahibi’nin MSS’nin yazılı ve
      mevzuata uygun olarak yapılmasıyla ilgili yükümlülükleri saklıdır.). Rezervasyon Talebi’nin onaylanması hukuken,
      Kiracı’nın ilettiği icabın kabul edilmesi niteliğindedir. Bir Rezervasyon Talebi’ni onayladığınızda Hizmet
      Sağlayıcı sizi ve Kiracı’yı e-posta, kısa mesaj veya bildirim yoluyla ya da Site üzerinden bilgilendirecektir.
