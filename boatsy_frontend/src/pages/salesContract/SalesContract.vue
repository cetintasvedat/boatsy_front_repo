      Zamanaşımı Tarihi'ne kadar veya Giriş'te doğrudan Tekne Sahibi tarafından tahsil edebilir (Kalan Ödeme’nin
      Zamanaşımı Tarihi’ne kadar Site üzerinden tahsil edilmesi gereken durumda, Kalan Ödeme zamanında ödenmez ise Tekne
      Sahibi Rezervasyon’u iptal edebilir; bu durumda Kiracı’nın Rezervasyon için yaptığı herhangi bir ödeme (Ön Ödeme
      ve varsa sair ödemeler) Kiracı’ya iade edilmez, ve Kiracı’nın Hizmet Sağlayıcı ya da Tekne Sahibi’nden – sınırlama
      olmaksızın, bu Sözleşme’de / Şartlar’da yazan haklar da dahil olmak üzere – herhangi bir talep hakkı olmaz).
      <br>
      <br>
      Rezervasyon Talebi’nin iletildiği anda Zaman Aşımı Tarihi’ne girilmiş ya da bu tarih geçilmiş ve Tekne Sahibi
      Kalan Ödeme’yi Zaman Aşımı Tarihi’ne kadar tahsil etmeyi tercih etmiş ise, yukarıda belirtilen ödeme sürecinde
      Rezervasyon Bedeli’nin tamamı tahsil edilir (Eğer Ön Ödeme Rezervasyon Talebi’nin iletilmesi sırasında alınamaz
      ise Rezervasyon Talebi iletilmeyecek ya da sistem tarafından kabul edilmeyecek, iletildi ya da kabul edildi ise de
      daha sonra iptal edilecektir). Rezervasyon Talebi’nin gönderildiği an ile Giriş Tarihi arasındaki süre yirmi dört
      (24) saatten fazla ise, Tekne Sahibi’ne Rezervasyon’u onaylamak ya da reddetmek için yirmi dört (24) saat verilir.
      Bahsi geçen süre yirmidört (24) saatten az ise, Tekne Sahibi’ne Rezervasyon Talebi’ni onaylamak ya da reddetmek
      için Giriş Tarihi’ne (00:00) kadar süre tanınır. Eğer Tekne Sahibi bu zaman dolana kadar hiçbir aksiyon almazsa
      Rezervasyon Talebi otomatik olarak iptal edilir.
