      başlaması durumunda, Tekne Sahibi ve Kiracı sorunu kendi aralarında çözmeye çalışır. Eğer anlaşmaya varılamazsa
      Kiracı Hizmet Sağlayıcı’yı bilgilendirir. Dört (4) saati aşan gecikmelerde Kiracı Rezevasyon’u iptal etme hakkına
