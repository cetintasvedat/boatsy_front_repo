      Para Birimi
      <br>
      <br>
      Para birimi konusunda işbu Sözleşme'de aşağıdaki ifadeler aşağıdaki tanımlara sahiptir:
      <br>
      <br>
      "Rezervasyon Para Birimi" Kiracı’nın bir Rezervasyon için ödeme yapmak zorunda olduğu para birimini ifade eder.
      Kiracı bir Rezervasyon Talebi gönderdiği anda Hizmet Sağlayıcı İnternet Sitesi’nin çalışma mantığına ve ÖHS'nin
      altyapısına göre Rezervasyon Para Birimi’ni seçer. Servis Sağlayıcı Rezervasyon Para Birimi olarak sadece belirli
      bir sayıda para birimini destekler. Bir Rezervasyon için Rezervasyon Para Birimi ile İlan Para Birimi farklı
      olabilir.
      <br>
      <br>
      "Baz Para Birimi" Tekne Sahibi’nin bir Tekne veya Deneyim ilanı verirken seçtiği para birimini ifade eder. Hizmet
      Sağlayıcı pazarın dinamiklerine ve Tekne’nin konumuna göre belirli bir sayıda para birimini Baz Para Birimi olarak
      sunar. İlan Para Birimi ve Rezervasyon Para Birimi ile Baz Para Birimi’nin farklı olması durumunda, Rezervasyon
      Bedeli o anki kurdan diğerlerine çevrilir (hangi kurun esas alınacağını Hizmet Sağlayıcı kendi serbest takdirine
      göre belirler). Ekstra Hizmetlerin Bedeli her zaman Baz Para Birimi cinsinden ödenir. Ön Ödeme ile Kalan Ödeme’nin
      farklı anlarda alınması durumunda Kalan Ödeme Baz Para Birimi olarak hesaplanır ve/veya tutulur ve ödemenin elden
      yapılmadığı durumlarda, ödeme anında o anki kurdan Rezervasyon Para Birimi’ne çevrilir. Ödemenin elden yapıldığı
      durumlarda ödeme Baz Para Birimi cinsinden yapılır. Ancak Tekne Sahibi kabul ederse başka bir para birimi ile de
      elden ödeme yapılabilir, bu durumda Kiracı ve Tekne Sahibi’nin anlaştığı kur uygulanır (eğer hangi kur üzerinde
      anlaşıldığı konusunda ihtilaf var ise, Hizmet Sağlayıcı’nın serbest takdirine göre belirteceği kur hem Kiracı hem
      de Tekne Sahibi açısından bağlayıcı olacaktır).
      <br>
      <br>
      "İlan Para Birimi" Kiracı tarafından kendisine sunulan alternatifler arasından seçilen ve arama panelinde
      listelenen Tekneler’in veya Deneyimler’in Rezervasyon Bedelleri’nin gösterildiği para birimidir. Tekne Sahibi
      tarafından Baz Para Birimi cinsinden belirlenen Rezervasyon Bedeli arama panelinde ve İlan sayfasında o anki
      kurdan İlan Para Birimi’ne çevrilir.
      <br>
      <br>
      "Avro" Avrupa Birliği’nin resmi para birimini ifade eder. Bu Sözleşme kapsamında, Avro’nun TL karşılığının
      tespitinde hangi kurun esas alınacağını Hizmet Sağlayıcı kendi serbest takdirine göre belirleyecektir.
      <br>
      <br>
      Ödemelerin her biri ve tümü Rezervasyon Para Birimi cinsinden yapılır. Tekne’nin Rezervasyon Bedeli’nin Site’de
      tutulduğu Baz Para Birimi, Rezervasyon Para Birimi’nden farklı ise, Baz Para Birimi ödeme anında Rezervasyon Para
      Birimi’ne çevrilir.
      <br>
      <br>
      Üyeler, Hizmet Sağlayıcı’nın Rezervasyon Para Birimi ve Baz Para Birimi seçeneklerini (i) herhangi bir İlan için
      genel olarak, ya da (ii) İlan’ı yayınlayan/görüntüleyen Tekne Sahibi’ne ve Kiracı’ya dair özeliklere göre özel
      olarak, ya da (iii) tamamen kendi serbest takdirine göre sınırlayabileceğini peşinen kabul eder. Tekne Sahibi,
      seçtiği Baz Para Birimi’nin bulunduğu ülkenin mevzuatına uygun olmasından sorumlu olup, herhangi bir Tekne ya da
      Deneyim için seçtiği Baz Para Birimi ilgili ülke mevzuatına aykırı ise, bu aykırılıktan doğabilecek tüm süreçlerle
      muhatap olacak, Hizmet Sağlayıcı’yı doğabilecek her türlü zarardan ari/ber’i kılacak ve de hem Kiracı’nın hem de
      Hizmet Sağlayıcı’nın uğradığı tüm zararları tazmin edecektir.
