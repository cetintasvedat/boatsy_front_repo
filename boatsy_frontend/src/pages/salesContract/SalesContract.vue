      Sağlayıcı tarafından belirtilmedikçe, Zamanaşımı Tarihi’ne kadar yapılması gerekmektedir. Her halükarda Zamanaşımı
      Tarihi itibarıyla Rezervasyon kesinleşir; iptal durumunda ücret iadesi yapılmaz; devam etmekte olan ücretsiz iptal
