      <br>
      Tekne Sahibi, Üye’ye, Üye’nin talep edip etmemesinden bağımsız olarak, ödeme öncesinde Opsiyon verebilir. Bu özel
      durumda başka bir Rezervasyon Onayı’na gerek duyulmaz ve ödemenin yapılması ile MSSTekne Sahibi ve Kiracı arasında
      kurulmuş olur. Opsiyon’un ne kadar süreyle geçerli olacağını Tekne Sahibi belirler.
      <br>
      <br>
      Tekne Sahibi ve Kiracı, dilerlerse MSS’yi yazılı şekilde tekrar onaylayabilirler (mevzuat uyarınca sözleşme
      imzalanması gerekli ise Tekne Sahibi MSS’nin imzalanmasını sağlamakla yükümlüdür). Hizmet Sağlayıcı MSS’ye veya
      Hizmet Sağlayıcı ve Kiracı arasında imzalanacak herhangi bir sözleşmeye taraf değildir ve bu sözleşmelerin şart ve
