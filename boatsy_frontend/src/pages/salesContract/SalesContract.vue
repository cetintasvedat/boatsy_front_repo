      <br>
      <br>
      Her bir İlan’a ilişkin Tam Rezervasyon Bedeli’ne karar vermek, ve Tam Rezervasyon Bedeli’nin İlan’da (ya da
      İlan’la ilgili olarak) Türkiye’deki uygulanabilir olan tüketici mevzuatına uygun bir şekilde yayınlanmasını
      sağlamak Tekne Sahibi’nin sorumluluğudur. Bir Kiracı’nın Tekne Sahibi’nin Tekne’si ya da Deneyim’ine yönelik bir
      Rezervasyon talep ettiğinde Tekne Sahibi’nin söz konusu Rezervasyon için daha önceden belirlenmiş olan Tam
      Rezervasyon Bedeli ve bileşenlerini yukarı yönlü olarak değiştiremeyeceğini her bir Tekne Sahibi Hizmet
      Sağlayıcı’ya ve Kiracılar’a peşinen, kabul, beyan ve taahhüt eder. Bu durumun tek istisnası minimum kiralama
      süresinin altına yapılan Rezervasyonlar’dır. Bu özel durumda, Rezervasyon Bedeli minimum kiralama süresi
      Rezervasyon Bedeli’ni aşmayacak şekilde günlük Rezervasyon Bedeli artırılabilir.
      <br>
      <br>
      Rezervasyon Talebi gönderebilmek için Kiracı’nın ödeme bilgilerini girmesi gerekir. Ödeme, kredi kartı, banka
      kartı ya da Hizmet Sağlayıcı tarafından kabul edilen herhangi başka bir ödeme yöntemi ile yapılabilir. Ödemenin
      kredi kartı ile yapılması durumunda Rezervasyon Talebi tamamlanmadan önce bu karta (tüm kira bedeline kadar)
      provizyon koyulabilir veya kredi kartı bilgileri saklanabilir. Ön Ödeme’ye eşit tutar Rezervasyon Talebi sırasında
      doğrudan çekilebilir ya da bloke edilebilir. Kredi kartı bilgilerinin saklanması ancak ödemenin alınmaması veya
      provizyon koyulması durumunda, Ön Ödeme Rezervasyon Onayı’nda alınır. Ödemenin banka kartı ya da herhangi başka
      ödeme yöntemi ile yapılması durumunda Ön Ödeme Rezervasyon Talebi sırasında tahsil edilecektir. Her halükârda, Ön
      Ödeme alınamaz ise Hizmet Sağlayıcı’nın Rezervasyon tamamlanmamış gibi hareket etme, ve ayrıca, dilediği zaman
      (kendisi ya da Tekne Sahibi aleyhine) herhangi bir tazminat yükümlülüğü doğmaksızın Rezervasyon’u iptal etme hakkı
      saklıdır.
